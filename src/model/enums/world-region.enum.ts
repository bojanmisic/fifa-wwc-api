/**
 * Enum representing the different world regions.
 */
export enum WorldRegion {
    /**
     * Africa region.
     */
    Africa = "Africa",
  
    /**
     * Americas region.
     */
    Americas = "Americas",
  
    /**
     * Asia region.
     */
    Asia = "Asia",
  
    /**
     * Europe region.
     */
    Europe = "Europe",
  
    /**
     * Oceania region.
     */
    Oceania = "Oceania",
  
    /**
     * Antarctica region.
     */
    Antarctica = "Antarctica",
  
    /**
     * Polar region, often considered for areas around the poles.
     */
    Polar = "Polar",
  }